class Person {
  String ext;
  String lastName;
  String firstName;
  String cellPhone;
  String directOffice;
  String homePhone;
  String email;
  String jobTitle;
  String department;
  String id;
  int key;

  Person({
    required this.ext,
    required this.lastName,
    required this.firstName,
    required this.cellPhone,
    required this.directOffice,
    required this.homePhone,
    required this.email,
    required this.jobTitle,
    required this.department,
    required this.id,
    required this.key,
  });
}